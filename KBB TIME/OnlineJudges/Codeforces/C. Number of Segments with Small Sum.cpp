#include <bits/stdc++.h>
using namespace std;
#define kien long long
#define JAV main
#define Million 1000000
#define NT 10000000
#define MOD 1000000007
kien n, k, s, ans, l = 1, a[1000000];

JAV()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    cin >> n >> k;
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
    }

    int r = 1;
    while (r <= n)
    {
        s += a[r]; 
        while (s > k and l <= r)
        {
            s -= a[l]; 
            l++;
        }
        ans += (r - l + 1);
        r++; 
    }

    cout << ans; 
}
